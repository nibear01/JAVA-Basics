public class ArithmaticOperator {
    public static void main(String[] args) {
        int a = 22;
        double b = 4;
        
        //addition:
        System.out.println(a+b);

        //subtraction:
        System.out.println(a-b);

        //division:
        System.out.println(a/b);

        //multiplication:
        System.out.println(a*b);

        //modulus(mod) remainder:
        System.out.println(a%b);

    }
}