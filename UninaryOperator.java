public class UninaryOperator {
    public static void main(String[] args) {
        int num = 100;

        num++; //increment
        System.out.println(num);

        num--; //decrement
        System.out.println(num);

        System.out.println(num++);  //it will increase 1 in the next print 
        System.out.println(num);   
    }
}
